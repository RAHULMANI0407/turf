import type { VercelRequest, VercelResponse } from "@vercel/node";
import crypto from "crypto";
import { db } from "./lib/firebase.js";

export default async function handler(req: VercelRequest, res: VercelResponse) {
  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  const {
    razorpay_order_id,
    razorpay_payment_id,
    razorpay_signature,
  } = req.body;

  // üîç LOG INPUT (temporary)
  console.log("VERIFY PAYMENT BODY:", req.body);

  if (!razorpay_order_id || !razorpay_payment_id || !razorpay_signature) {
    return res.status(400).json({ error: "Missing payment details" });
  }

  const secret = process.env.RAZORPAY_KEY_SECRET;
  if (!secret) {
    return res.status(500).json({ error: "Secret missing" });
  }

  const body = razorpay_order_id + "|" + razorpay_payment_id;

  const expectedSignature = crypto
    .createHmac("sha256", secret)
    .update(body)
    .digest("hex");

  if (expectedSignature !== razorpay_signature) {
    return res.status(400).json({ error: "Invalid signature" });
  }

  // ‚úÖ PAYMENT VERIFIED ‚Äî SAVE TO FIREBASE
  await db.collection("payments").doc(razorpay_order_id).set({
    orderId: razorpay_order_id,
    paymentId: razorpay_payment_id,
    status: "confirmed",
    createdAt: Date.now(),
  });

  return res.status(200).json({ success: true });
}
